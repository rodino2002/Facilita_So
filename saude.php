                                <strong><h2>1. Água </h2></strong>
                                      A água é um dos principais remédios que a natureza oferece e 80% do nosso cérebro. Além disso, esse líquido regula uma série de funções do organismo. A água contribui para o equilíbrio término, para o bom funcionamento do intestino, evita cálculos renais, entre outros problemas.</br><a href="#" name="ar"></a>

                                A água participa ainda de todos os processos químicos que acontecem no organismo. Nosso corpo funciona devido aos estímulos elétricos produzidos (e recebidos) pelo sistema nervoso. A água possui minerais que a tornam uma condutora de eletricidade. Portanto, ela permite a comunicação entre as células e o bom funcionamento dos nossos sistemas.</br>
                                Portanto, tomar água em grande quantidade é fundamental para manter ou restabelecer a saúde.</br></br>
