                                <strong><h2>7. Luz Solar</h2></strong>
            Durante anos, a comunidade científica alertou a população sobre os perigos de tomar sol. No entanto, hoje as pesquisas mostram que, embora a hiperexposição realmente não seja recomendada, o contato diário com a luz solar é muito importante para a manutenção da saúde.</br>
            A luz solar promove uma série de reações químicas no organismo. Quando a pele é exposta ao sol, seus raios desencadeiam mecanismos de controle da pressão arterial, por exemplo. Também não podemos nos esquecer da vitamina D, que na verdade é um hormônio que traz muitos benefícios e é associado à prevenção de uma série de doenças.</br>
            Portanto, a exposição controlada à luz solar, sem uso de protetores químicos, deve fazer parte da nossa rotina.</br></br>
