                                <strong><h2>3. Alimentação Saudável</h2></strong>
            A alimentação é um dos principais remédios naturais. Ela precisa conter todos os macronutrientes (carboidratos, proteínas e gorduras) e micronutrientes (vitaminas e minerais) que o corpo utiliza para obter energia, produzir novas células e regular suas funções.</br>
            Manter uma alimentação saudável é mais simples do que as pessoas imaginam. Porém, como nosso paladar está habituado e a sentir prazer com opções que não fazem bem ao corpo, a mudança de hábitos pode ser desafiadora.</br></br><a href="#" name="exercicio"></a>

            alimentação saudável</br>
            A alimentação pode ser fonte de cura ou também provocar uma série de doenças. Por isso, vale a pena mudar o cardápio, nem que para isso seja necessário passar por um processo gradual de aprender a ingerir alimentos saudáveis e eliminar os que não fazem bem.</br></br>
