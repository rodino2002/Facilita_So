            <strong><h2>4. Exercício</h2></strong>
            O exercício é outro fator fundamental para uma boa saúde. Além da sensação de bem-estar obtida através da produção de endorfinas, ele ajuda a dormir melhor, a manter o peso estável, contribui para evitar e a superar a depressão e está relacionado à prevenção do câncer, entre outros benefícios.</br>
            A atividade física ainda preserva a massa muscular, contribuindo para manter a força física, proteger as articulações e favorecer a mobilidade. Também não podemos nos esquecer de seu papel no fortalecimento do coração, estímulo ao bom funcionamento do intestino, aumento da imunidade e preservação das funções cognitivas, devido à produção de neurotransmissores que beneficiam o tono psíquico e o humor.</br><a href="#" name="repouso"></a>
            A quantidade de exercício recomendada pela Organização Mundial da Saúde é de 150 minutos semanais de atividades moderadas ou 75 minutos de atividade intensa. Portanto, bastam 5 sessões de 30 minutos por semana para melhorar muito sua saúde e qualidade de vida.</br></br>
            <hr class="bg-primary">
                             </div>
                             <div class="col-lg-6 col-sm-6">
                                 <strong><h2>5. Repouso</h2></strong>
            O repouso é tão importante quanto o exercício. O corpo e o cérebro precisam desse período de descanso para se recuperarem do esforço do dia, organizar as informações recebidas, produzir novas células e hormônios importantes para regular o funcionamento do organismo.</br>
            A falta do sono reparador está associada a uma série de problemas de saúde física e mental. A insônia aumenta as chances de desenvolver doenças como diabetes, problemas cardiovasculares, inflamações, depressão e outros transtornos mentais.</br><a href="#" name="temperanca"></a>
            Portanto, se você não consegue dormir bem, é importante se esforçar para começar a ter noites reparadoras. A higiene do sono é um conjunto de ações que podem ajudá-lo a condicionar seu corpo e mente para esse momento de descanso que é tão importante para nossa saúde e bem-estar.</br></br>
            <hr class="bg-primary">
                             </div>
                             <div class="col-lg-6 col-sm-6">
                                   <strong><h2>6. Temperança</h2></strong>
            A temperança é uma palavra que muitas pessoas preferem substituir por equilíbrio. Trata-se da capacidade de usar com moderação aquilo que nos faz bem e praticar a abstinência em relação ao que provoca doenças. Alguns exemplos de hábitos que precisam ser eliminados são o tabagismo, uso de substâncias químicas, consumo de álcool e alimentos adoçados, entre outros.</br><a href="#" name="luz"></a>
            Embora alguns desses hábitos sejam aceitos socialmente e culturalmente, é importante saber que eles são fatores causadores de doenças. Um exemplo é o consumo de álcool, tão presente no convívio social. Na verdade, não existe um nível seguro para ingestão de bebidas alcoólicas. Mesmo em doses moderadas, elas aumentam os riscos de câncer, por exemplo.</br></br>
