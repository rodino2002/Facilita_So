           <strong><h2>8. Confiança em Deus</h2></strong>
            As diferentes academias que pesquisam e divulgam a Medicina do Estilo de Vida apresentam esse remédio natural de forma diferente. Algumas se referem a ele como gratidão, outras como fé e ainda há os que preferem denominá-lo espiritualidade.</br>
            O fato é que, independentemente de sua religião, vários estudos demonstram que a fé ou espiritualidade é capaz de gerar significado e valor para a vida humana, além de influenciar positivamente na saúde e processos de cura.</br>
            Mário Borba, diretor científico de um projeto da Sociedade Brasileira de Cardiologia que estuda justamente a relação entre espiritualidade e a Medicina, afirma que “já não temos dúvidas de que a fé contribui para a saúde. Queremos entender melhor agora até onde vão seus efeitos e de que forma ela os propicia”.</br>
